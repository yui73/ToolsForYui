headers = random.choice(headers_list)


class Chapter:
    # 章节名
    name = ""
    # 章节连接
    link = ""
    # 章节内容
    content = ""

    def __init__(self, name, link, content):
        self.name = name
        self.link = link
        self.content = content

    def getName(self):
        return self.name

    def getLink(self):
        return self.link

    def getContent(self):
        return self.content


def getList():
    req = requests.get("http://m.quanzhifashi.com/novel/79431/all.html",headers=headers, timeout=10)
    html = req.text
    soup = BeautifulSoup(html, features="lxml")
    chapters = soup.find_all("li")
    # list = soup.find_all("select","clist_select")
    list = soup.find_all("option")
    for i in list:
        if (not i.has_attr('selected')):
            # value = i["value"]
            time.sleep(random.uniform(0.5, 1))
            # 获取下一页列表
            req_t = requests.get("http://m.quanzhifashi.com" + i["value"],headers=headers, timeout=10)
            html_t = req_t.text
            soup_t = BeautifulSoup(html_t, features="lxml")
            chapters_t = soup_t.find_all("li")
            chapters = chapters + chapters_t
    return chapters


def getContent(name, href):
    print("开始爬取：" + name)
    # 爬取第一页
    time.sleep(random.uniform(0.5, 1))
    req = requests.get("http://m.quanzhifashi.com" + href,headers=headers, timeout=10)
    html = req.text
    soup = BeautifulSoup(html, features="lxml")
    content = soup.find_all("p", "content_detail")
    all = ""
    for i in content:
        t = i.text.strip()
        # 不爬取回车
        all = all + t + "\n"
    # 爬取第二页
    time.sleep(random.uniform(0.5, 1))
    req = requests.get("http://m.quanzhifashi.com" + href[:len(href) - 5] + "/2.html",headers=headers, timeout=10)
    html = req.text
    soup = BeautifulSoup(html, features="lxml")
    content = soup.find_all("p", "content_detail")
    for i in content:
        t = i.text.strip()
        # 不爬取回车
        all = all + t + "\n"
    # print(all)
    print("爬取结束：" + name)
    return all


def getNoval(chapters):
    noval = []
    for i in chapters:
        href = i.a.get('href')
        name = i.a.text.strip()
        # 调用获取内容
        content = getContent(name, href)
        # 格式化内容
        chapter = Chapter(name, href, content)
        noval.append(chapter)
    return noval


